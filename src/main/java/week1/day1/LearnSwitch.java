package week1.day1;

public class LearnSwitch {
	public static void main(String[] args) {
		int day = 1;
		switch (day) {
		case 1:
			System.out.println("monday");
			break;
		case 2:
			System.out.println("Tuesday");
			break;
		default:
			System.out.println("other day");
			break;
		}
	}
}
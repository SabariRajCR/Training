package week1.day1;

public class BasicCalcMethod {
	
	public int Add(int a, int b) {
		
		int c = a + b;
		return c;
		
	}
	public int Sub(int a, int b) {
		
		int c = a - b;
		return c;
	}
	
	public int Mult(int a, int b) {
			
			int c = a * b;
			return c;
			
		}

	public int division(int a, int b) {
			
			int c = a / b;
			return c;
			
		}

}

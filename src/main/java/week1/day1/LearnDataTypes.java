package week1.day1;

public class LearnDataTypes {

	public static void main(String[] args) {
		// local variable -> no access specifier
		byte age = 100;
		System.out.println(age);
		short runs = 5000;
		int salary = 12345;
		// add L or l for long data type
		long accNum = 12345678901L;
		float weight = 45.5f;
		double balance = 1213121.12;
		char a = 'K';
		System.out.println(a);
		

	}

}





